import { CheckCircle2, XCircle, Clock, TrendingUp, Download, Scale } from "lucide-react";
import { BarChart, Bar, XAxis, YAxis, Tooltip, ResponsiveContainer, Cell } from "recharts";
import type { PredictionResult, UserInput } from "@/lib/model";
import WhatIfAnalysis from "./WhatIfAnalysis";
import FeatureWaterfall from "./FeatureWaterfall";
import OptimalMonth from "./OptimalMonth";
import FeatureImportanceBars from "./FeatureImportanceBars";
import { visaCategories, countries } from "@/lib/visaData";
import { MODEL_METRICS } from "@/lib/model";

interface Props {
  result: PredictionResult;
  input: UserInput;
  onNewPrediction?: () => void;
}

const PredictionResults = ({ result, input, onNewPrediction }: Props) => {
  const isCertified = result.predictedStatus === "Certified";
  const deniedProb = Math.round((1 - result.approvalProbability) * 100);
  const approvedProb = Math.round(result.approvalProbability * 100);

  const chartData = [
    { name: "Certified", value: approvedProb },
    { name: "Denied", value: deniedProb },
  ];

  // Risk level
  const riskLevel = approvedProb >= 75 ? "Low" : approvedProb >= 50 ? "Medium" : "High";
  const riskColor = riskLevel === "Low" ? "text-primary" : riskLevel === "Medium" ? "text-yellow-500" : "text-destructive";

  // Compare with similar
  const continentAvg = countries.find(c => c.region === (input.continent === "North America" ? "Americas" : input.continent))?.avgProcessingDays || 7.5;

  const handleDownloadPDF = () => {
    const content = `VISA PREDICTION REPORT\n${"=".repeat(40)}\n\nStatus: ${result.predictedStatus}\nApproval Probability: ${approvedProb}%\nConfidence: ${result.confidence}%\nProcessing Time: ${result.processingDays} days (${result.processingDaysLow}-${result.processingDaysHigh})\nRisk Level: ${riskLevel}\n\nGenerated by VisaPredict.ai`;
    const blob = new Blob([content], { type: "text/plain" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = "visa-prediction-report.txt";
    a.click();
    URL.revokeObjectURL(url);
  };

  return (
    <div className="mt-10 space-y-6 animate-fade-in">
      <h3 className="font-heading text-xl font-bold text-foreground text-center">
        AI Prediction Results
      </h3>

      <div className="grid gap-6 md:grid-cols-2">
        {/* Visa Status Card */}
        <div className="glass-card p-6 space-y-4">
          <div className="flex items-center gap-2 text-sm font-medium text-muted-foreground">
            <TrendingUp className="h-4 w-4" />
            Visa Status Prediction
          </div>
          <div className="flex items-center gap-3">
            {isCertified ? (
              <CheckCircle2 className="h-8 w-8 text-primary" />
            ) : (
              <XCircle className="h-8 w-8 text-destructive" />
            )}
            <span className={`font-heading text-2xl font-bold ${isCertified ? "text-primary" : "text-destructive"}`}>
              {result.predictedStatus}
            </span>
          </div>
          <div className="space-y-2">
            <div className="flex justify-between text-sm">
              <span className="text-muted-foreground">Confidence Level</span>
              <span className="font-heading font-bold text-foreground">{result.confidence}%</span>
            </div>
            <div className="h-3 w-full overflow-hidden rounded-full bg-secondary">
              <div
                className={`h-full rounded-full transition-all duration-1000 ease-out ${isCertified ? "bg-primary" : "bg-destructive"}`}
                style={{ width: `${result.confidence}%` }}
              />
            </div>
          </div>
          <div className="space-y-1 text-sm">
            <div className="flex justify-between">
              <span className="text-muted-foreground">Approval Probability</span>
              <span className="font-heading font-semibold text-primary">{approvedProb}%</span>
            </div>
            <div className="flex justify-between">
              <span className="text-muted-foreground">Denial Probability</span>
              <span className="font-heading font-semibold text-destructive">{deniedProb}%</span>
            </div>
          </div>
        </div>

        {/* Processing Time Card */}
        <div className="glass-card p-6 space-y-4">
          <div className="flex items-center gap-2 text-sm font-medium text-muted-foreground">
            <Clock className="h-4 w-4" />
            Processing Time Estimate
          </div>
          <div className="text-center py-2">
            <span className="font-heading text-4xl font-bold text-foreground">
              {result.processingDays}
            </span>
            <span className="ml-1 text-lg text-muted-foreground">± {result.processingDays - result.processingDaysLow} days</span>
          </div>
          <div className="rounded-lg bg-secondary p-4 text-center">
            <div className="text-xs text-muted-foreground mb-1">Expected Range</div>
            <div className="font-heading text-lg font-bold text-foreground">
              {result.processingDaysLow} – {result.processingDaysHigh} days
            </div>
          </div>
        </div>
      </div>

      {/* Risk Assessment */}
      <div className="glass-card p-6">
        <div className="flex items-center gap-2 text-sm font-medium text-muted-foreground mb-4">
          <Scale className="h-4 w-4" />
          Risk Assessment
        </div>
        <div className="flex items-center gap-4">
          <div className="flex-1">
            <div className="flex justify-between text-xs text-muted-foreground mb-2">
              <span>Low Risk</span>
              <span>Medium</span>
              <span>High Risk</span>
            </div>
            <div className="relative h-4 rounded-full overflow-hidden">
              <div className="absolute inset-0 bg-gradient-to-r from-primary via-yellow-500 to-destructive opacity-30" />
              <div
                className="absolute top-0 h-full w-4 rounded-full bg-foreground border-2 border-background transition-all duration-700"
                style={{ left: `${Math.max(2, Math.min(96, 100 - approvedProb))}%` }}
              />
            </div>
          </div>
          <span className={`font-heading text-lg font-bold ${riskColor}`}>{riskLevel}</span>
        </div>
      </div>

      {/* Probability Chart */}
      <div className="glass-card p-6">
        <div className="text-sm font-medium text-muted-foreground mb-4">Probability Distribution</div>
        <ResponsiveContainer width="100%" height={180}>
          <BarChart data={chartData} barSize={60}>
            <XAxis dataKey="name" tick={{ fill: "hsl(210 10% 55%)", fontSize: 12 }} axisLine={false} tickLine={false} />
            <YAxis domain={[0, 100]} tick={{ fill: "hsl(210 10% 55%)", fontSize: 12 }} axisLine={false} tickLine={false} unit="%" />
            <Tooltip
              contentStyle={{ background: "hsl(210 25% 11%)", border: "1px solid hsl(210 20% 18%)", borderRadius: 8, color: "hsl(180 10% 90%)" }}
              formatter={(value: number) => [`${value}%`, '']}
            />
            <Bar dataKey="value" radius={[6, 6, 0, 0]}>
              <Cell fill="hsl(170, 80%, 50%)" />
              <Cell fill="hsl(0, 70%, 50%)" />
            </Bar>
          </BarChart>
        </ResponsiveContainer>
      </div>

      {/* Compare with Similar Applications */}
      <div className="glass-card p-6">
        <div className="text-sm font-medium text-muted-foreground mb-4">Compare with Similar Applications</div>
        <div className="grid grid-cols-2 gap-4 sm:grid-cols-4">
          <div className="text-center">
            <div className="text-xs text-muted-foreground">Your Estimate</div>
            <div className="font-heading text-2xl font-bold text-primary">{result.processingDays}d</div>
          </div>
          <div className="text-center">
            <div className="text-xs text-muted-foreground">Same Education Avg</div>
            <div className="font-heading text-2xl font-bold text-foreground">
              {input.education === "Doctorate" ? "18" : input.education === "Master's" ? "22" : "30"}d
            </div>
          </div>
          <div className="text-center">
            <div className="text-xs text-muted-foreground">Same Region Avg</div>
            <div className="font-heading text-2xl font-bold text-foreground">{Math.round(continentAvg)}d</div>
          </div>
          <div className="text-center">
            <div className="text-xs text-muted-foreground">Overall Average</div>
            <div className="font-heading text-2xl font-bold text-foreground">28d</div>
          </div>
        </div>
      </div>

      {/* Feature Importance */}
      <FeatureImportanceBars />

      {/* Feature Waterfall */}
      <FeatureWaterfall input={input} result={result} />

      {/* What-If Analysis */}
      <WhatIfAnalysis baseInput={input} baseResult={result} />

      {/* Optimal Month */}
      <OptimalMonth />

      {/* AI Model Confidence */}
      <div className="glass-card p-6">
        <div className="text-sm font-medium text-muted-foreground mb-4">AI Model Confidence</div>
        <div className="grid grid-cols-2 gap-4 sm:grid-cols-4">
          <div className="text-center">
            <div className="font-heading text-2xl font-bold text-primary">{(MODEL_METRICS.classification.accuracy * 100).toFixed(1)}%</div>
            <div className="text-xs text-muted-foreground">Model Accuracy</div>
          </div>
          <div className="text-center">
            <div className="font-heading text-2xl font-bold text-foreground">{MODEL_METRICS.datasetSize.toLocaleString()}</div>
            <div className="text-xs text-muted-foreground">Training Samples</div>
          </div>
          <div className="text-center">
            <div className="font-heading text-2xl font-bold text-foreground">{MODEL_METRICS.regression.r2.toFixed(2)}</div>
            <div className="text-xs text-muted-foreground">R² Score</div>
          </div>
          <div className="text-center">
            <div className="font-heading text-2xl font-bold text-foreground">±{MODEL_METRICS.regression.mae.toFixed(1)}</div>
            <div className="text-xs text-muted-foreground">Days Error Margin</div>
          </div>
        </div>
      </div>

      {/* Action Buttons */}
      <div className="flex gap-3 justify-center">
        {onNewPrediction && (
          <button
            onClick={onNewPrediction}
            className="rounded-xl bg-primary px-6 py-3 font-bold text-primary-foreground hover:opacity-90 transition-all"
          >
            Try Another Estimate
          </button>
        )}
        <button
          onClick={handleDownloadPDF}
          className="inline-flex items-center gap-2 rounded-xl border border-border px-6 py-3 text-sm font-medium text-muted-foreground hover:bg-secondary hover:text-foreground transition-colors"
        >
          <Download className="h-4 w-4" /> Download Report
        </button>
      </div>
    </div>
  );
};

export default PredictionResults;
